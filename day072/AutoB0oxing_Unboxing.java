package day072;

public class AutoB0oxing_Unboxing {

	public static void main(String[] args) {
		//AutoBoxing
		Integer i=10;
		Double d=3.14;
		Float f= 3.14f;
		Character c= 'A';
		
		//auto Unboxing
		int ii=i;
		double dd=d;
		float ff=f;
		char cc=c;
		System.out.println(ii);
		System.out.println(dd);
		System.out.println(ff);
		System.out.println(cc);
		
		
	

	}

}
